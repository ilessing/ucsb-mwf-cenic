<h1 id="header">Screen [mwf.screen]</h1>

<div class="content">

<h2>Description</h2>

<p>The <strong>mwf.screen</strong> object provides dimensionality information about the static screen size.</p>

<h2>Functions &amp; Properties</h2>

<h3>Current (MWF 1.2)</h3>

<p>The following functions are available through <strong>mwf.screen</strong>:</p>

<ul>
<li><p><strong>mwf.screen.getHeight()</strong> The height of the device screen.</p></li>
<li><p><strong>mwf.screen.getWidth()</strong> The width of the device screen.</p></li>
<li><p><strong>mwf.screen.getPixelRatio()</strong> The pixel density ratio of the device screen (most are 1, iPhone Retina display is 2).</p></li>
</ul><p>The following attribute is also available through <strong>mwf.screen</strong>:</p>

<ul>
<li>
<strong>mwf.screen.cookieName</strong> The name of the cookie that assets/js/core/server.js writes.</li>
</ul><h3>Previous (MWF 1.0-1.1)</h3>

<p>This object did not exist before MWF 1.2.</p>

