<h1 id="header">Utilities [mwf.util]</h1>

<div class="content">

<p>Utility functions are encapsulated in the <strong>mwf.util</strong> namespace.</p>

<ul>
<li><p><strong>mwf.util.importCSS(webpath)</strong> Writes a link tag to the DOM with the web path in question to include the CSS file specified. This allows the dynamic loading of a script asynchronous from page load if desired, as well as allows the browser to best cache the item.</p></li>
<li><p><strong>mwf.util.importJS(webpath)</strong> Writes a script tag to the DOM with the web path in question to include the script specified. This allows the dynamic loading of a script asynchronous from page load if desired, as well as allows the browser to best cache the item.</p></li>
</ul>

</div>